// switch

const color = 'green'

switch (color) {
  case 'blue':
    console.log('u are my blue friend')
    break

  case 'dark':
    console.log('u are my dark friend')
    break

  case 'yellow':
    console.log('u are my yellow friend')
    break

  case 'green':
    console.log('u are my green friend')
    break

  default:
    console.log('u r not my friend')
    break
}
